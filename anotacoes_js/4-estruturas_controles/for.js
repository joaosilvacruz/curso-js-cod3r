//Estrutura com o while
let contador = 1
while(contador <= 10){
    console.log(`contador = ${contador}`)
    contador++
}

//Estrutura com o for
for (let i = 1; i<=10; i++){
    console.log(`contador = ${i}`)
}

//Percorrendo um array com for
const notas = [6, 5, 4, 2, 1, 10]
for(let i = 0; i < notas.length; i++){
    console.log(`nota = ${notas[i]}`)
}